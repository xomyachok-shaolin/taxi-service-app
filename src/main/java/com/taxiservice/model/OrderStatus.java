package com.taxiservice.model;

/**
 * Перечисление статусов заказа такси.
 * Отражает жизненный цикл заказа от создания до завершения.
 */
public enum OrderStatus {

    /** Заказ создан, ожидает назначения водителя */
    CREATED,

    /** Водитель назначен, ожидает подтверждения */
    ASSIGNED,

    /** Водитель в пути к клиенту */
    EN_ROUTE,

    /** Клиент в машине, поездка началась */
    IN_PROGRESS,

    /** Поездка завершена */
    COMPLETED,

    /** Заказ отменён */
    CANCELLED
}
