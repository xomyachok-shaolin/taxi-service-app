package com.taxiservice.model;

/**
 * Перечисление ролей пользователей системы.
 * Определяет уровень доступа и функциональные возможности каждого пользователя.
 */
public enum Role {

    /** Клиент — может заказывать такси и просматривать историю заказов */
    CLIENT,

    /** Водитель — просматривает назначенные заказы, обновляет статус поездки */
    DRIVER,

    /** Диспетчер — управляет заказами, назначает водителей */
    DISPATCHER,

    /** Администратор — управляет пользователями и ролями, просматривает статистику */
    ADMIN
}
